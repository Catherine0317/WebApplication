                <p>During the pandemic period of COVID-19, the real estate industry in New York City has been largely impacted by uncertainty and distrust between buyers and sellers.
                    Even more, economic fluctuations and other uncertain social factors also greatly affected the balance of the real estate industry in NYC, which led the market into a more unsafe and faith-absent situation.
                    Therefore, our team would like to create this website system regarding New York City real estate.
                    This system will link to a database, recording all sold property in NYC from 2018 to 2019.
                    Our mission is to build this system, including information about both the property owners and the property itself and relating property buyers and sellers closely.
                    Customers could find property owners' name, address, zip code, tax category, properties' sales data, and square footage etc, will be involved in, in order to benefit the whole real estate market in NYC. </p>
